#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	a;

	if (nb == 0)
	{
		write (1, "0", 1);
		return ;
	}
	else if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
	}
	a = nb % 10;
	a += '0';
	if (nb > 9)
		ft_putnbr(nb / 10);
	write (1, &a, 1);
}
